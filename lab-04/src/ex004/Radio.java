package ex004;

class Radio {
    public void ligar() {
        System.out.println("Rádio ligado.");
    }

    public void sintonizar(String estacao) {
        System.out.println("Sintonizando na estação: " + estacao);
    }

    public void desligar() {
        System.out.println("Rádio desligado.");
    }
}